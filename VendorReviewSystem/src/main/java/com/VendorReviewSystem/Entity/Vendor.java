package com.VendorReviewSystem.Entity;

public class Vendor {
	
	private int vendorid;
	private String vendorname;
	private String email;
	private String website_id;
	private String vendortype;
	private String cusinetype;
	private String uploadmenucard;
	private String uploadphoto;
	private String status;
	public String getStatus() {
		return status;
	}
	public void setStatus(String status) {
		this.status = status;
	}
	public int getVendorid() {
		return vendorid;
	}
	public void setVendorid(int vendorid) {
		this.vendorid = vendorid;
	}
	public String getVendorname() {
		return vendorname;
	}
	public void setVendorname(String vendorname) {
		this.vendorname = vendorname;
	}
	public String getEmail() {
		return email;
	}
	public void setEmail(String email) {
		this.email = email;
	}
	public String getWebsite_id() {
		return website_id;
	}
	public void setWebsite_id(String website_id) {
		this.website_id = website_id;
	}
	public String getVendortype() {
		return vendortype;
	}
	public void setVendortype(String vendortype) {
		this.vendortype = vendortype;
	}
	public String getCusinetype() {
		return cusinetype;
	}
	public void setCusinetype(String cusinetype) {
		this.cusinetype = cusinetype;
	}
	public String getUploadmenucard() {
		return uploadmenucard;
	}
	public void setUploadmenucard(String uploadmenucard) {
		this.uploadmenucard = uploadmenucard;
	}
	public String getUploadphoto() {
		return uploadphoto;
	}
	public void setUploadphoto(String uploadphoto) {
		this.uploadphoto = uploadphoto;
	}
	
	
	

}
